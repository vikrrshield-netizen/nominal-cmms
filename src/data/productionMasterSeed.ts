function ddmmyy(d: Date): string {
  const p = (n: number) => String(n).padStart(2, '0');
  return `${p(d.getDate())}${p(d.getMonth() + 1)}${p(d.getFullYear() % 100)}`;
}

export function materialBatch(materialNumber: string, intakeDate: Date = new Date(), suffix = ''): string {
  const num = materialNumber.padStart(2, '0');
  const sfx = suffix ? `-${suffix.toUpperCase()}` : '';
  return `nk${num}${ddmmyy(intakeDate)}${sfx}`;
}

export function productBatch(productNumber: string, startDate: Date = new Date()): string {
  const num = productNumber.padStart(3, '0');
  return `sk${num}${ddmmyy(startDate)}`;
}

